package cn.kis2.extendsdemo1;

public class People {
    // 父类
    // 继承的好处：代码复用,减少重复代码
    private String name;
    private char sex;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public char getSex() {
        return sex;
    }

    public void setSex(char sex) {
        this.sex = sex;
    }
}
